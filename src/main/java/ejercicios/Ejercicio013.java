package ejercicios;

public class Ejercicio013 {

    public void mostrarPrimeros20Pares() {
        System.out.print("Los primeros veinte números pares: ");

        int num = 2;
        for (int i = 0; i < 20; i++) {
            System.out.print(num + " ");
            num += 2;
        }
    }
}


